// cadenas de texto
saludo = "hola amigos de la web ";

pregunta = "que tal va todo?";

frase = saludo + pregunta;

document.write(frase)

//numeros 
numero1 = 50;

numero2 = 10; 

resultado = numero1 + numero2;

document.write(resultado)

//forzar strings 

numero1 = 50;

numero2 = 10; 

resultado =" " + numero1 + numero2;

document.write(resultado)

//metodo concat (solo cadenas de texto)

saludo = "hola amigos de la web ";

pregunta = "que tal va todo?";

frase = saludo.concat(pregunta);

document.write(frase)

//metodo de backticks

nombre = "bruno acosta";

frase = `soy ${nombre} y estoy caminando`;

document.write(frase)

//escape de backticks comillas simples

frase = 'soy `bruno` y estoy caminando'

document.write(frase)

//escape de backticks comillas dobles

frase = "soy `bruno` y estoy caminando"

document.write(frase)

